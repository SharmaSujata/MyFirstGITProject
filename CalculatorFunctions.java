
public class CalculatorFunctions {
	
	public Integer add(int a,int b)
	{
	return (a+b);
	}
	public Integer sub(int a,int b)
	{
	return (a-b);
	}

	public Integer mul(int a,int b)
	{
	return (a*b);
	}

	public Integer div(int a,int b)
	{
	return (a/b);
	}
	public Float mod(int a,int b)
	{
	return (float) (a % b);
	}


	

}
